-- ============================================================
-- PS5商城 省市区 + 收货地址表
-- ============================================================
USE `cursor`;

-- 省市区表
CREATE TABLE IF NOT EXISTS `shop_region` (
  `region_id`   INT          NOT NULL AUTO_INCREMENT,
  `parent_id`   INT          NOT NULL DEFAULT 0    COMMENT '父级ID：0=省级',
  `region_name` VARCHAR(100) NOT NULL              COMMENT '地区名称',
  `level`       TINYINT      NOT NULL              COMMENT '1=省 2=市 3=区县',
  `sort_order`  INT                   DEFAULT 0    COMMENT '排序号',
  PRIMARY KEY (`region_id`),
  INDEX `idx_parent_id` (`parent_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='省市区数据';

-- 收货地址表
CREATE TABLE IF NOT EXISTS `shop_address` (
  `address_id`     BIGINT       NOT NULL AUTO_INCREMENT,
  `user_id`        BIGINT       NOT NULL              COMMENT '用户ID',
  `receiver_name`  VARCHAR(50)  NOT NULL              COMMENT '收货人姓名',
  `phone`          VARCHAR(20)  NOT NULL              COMMENT '联系电话',
  `province_id`    INT          NOT NULL DEFAULT 0,
  `province_name`  VARCHAR(50)  NOT NULL DEFAULT '',
  `city_id`        INT          NOT NULL DEFAULT 0,
  `city_name`      VARCHAR(50)  NOT NULL DEFAULT '',
  `district_id`    INT          NOT NULL DEFAULT 0,
  `district_name`  VARCHAR(50)  NOT NULL DEFAULT '',
  `detail`         VARCHAR(200) NOT NULL              COMMENT '详细地址',
  `remark`         VARCHAR(200)          DEFAULT NULL COMMENT '备注',
  `is_default`     TINYINT               DEFAULT 0    COMMENT '0否 1是',
  `create_time`    DATETIME              DEFAULT NULL,
  `update_time`    DATETIME              DEFAULT NULL,
  PRIMARY KEY (`address_id`),
  INDEX `idx_user_id` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户收货地址';

